		<h2>What do colonoscopies look for?</h2>
		<p>The purpose of a colonoscopy is to look inside your colon for any polyps.</p>
		<p>Before the procedure starts, you will receive some medicine to make you sleepy to help you avoid discomfort from the procedure.</p>
		<p>The doctor will begin the colonoscopy with a rectal examination. The doctor will then insert the scope into the anus and inflate the bowel with a stream of air to better view the surface.</p>

