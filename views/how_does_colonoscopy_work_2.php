		<h2>How Does A Colonoscopy Work? (Part 2)</h2>

		<p>The endoscope is a thin, flexible tube that has a light and a camera at the tip. Images from the camera go to a TV monitor, allowing the doctor to see the inside of your colon. The doctor will look for polyps and remove any that are found. A colonoscopy usually takes around 20-30 minutes to complete.</p>

