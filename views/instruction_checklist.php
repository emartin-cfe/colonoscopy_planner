		<h2>Printable Instructions</h2>
		<p>Here's a checklist of things to do as you prepare for your colonoscopy. Please print this page and use it as your planner. As you do each one, check it off the list by marking an "X" in each box. Make sure each box has been checked prior to coming in for your procedure.</p>

		<h2>Before you start</h2>
		<ul class="hidden_bullets">
			<li><input type="checkbox"> Read this web booklet carefully.</li>
			<li><input type="checkbox"> Make sure you have your bowel prep kit.</li>
			<li><input type="checkbox"> If needed, talk to your doctor about your medications.</li>
		</ul>

		<h2>One day before your colonoscopy</h2>
		<ul class="hidden_bullets">
			<li><input type="checkbox"> Eat breakfast - clear liquids only, no solid food.</li>
			<li><input type="checkbox"> Eat lunch - clear liquids only, no solid food.</li>
			<li><input type="checkbox"> Eat dinner - clear liquids only, no solid food.</li>
			<li><input type="checkbox"> Take your bowel prep in the evening as instructed.</li>
			<li><input type="checkbox"> Drink at least 12 tall glasses of clear liquids throughout the day.</li>
		</ul>

		<h2>Day of your colonoscopy</h2>
		<ul class="hidden_bullets">
			<li><input type="checkbox"> If your doctor prescribed a "split prep", take the <b>second part</b> of bowel prep as instructed, starting four hours before your colonoscopy.</li>
			<li><input type="checkbox"> Drink two tall glasses of clear sports drink prior to your appointment.</li>
			<li><input type="checkbox"> Report for your colonoscopy as instructed.</li>
		</ul>
