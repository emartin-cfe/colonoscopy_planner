		<h2 class="top">Instructions: day of your colonoscopy</h2>

		<h2>What you eat:</h2>
		<p>You must not eat any solid foods prior to your colonoscopy, even if your colonoscopy is scheduled for the afternoon.</p>

		<h2>What you drink:</h2>
		<p>You must drink only clear liquids before your colonoscopy. Drink two full cups of clear sports drink, such as gatorade, prior to your colonoscopy.</p>

		<h2>Taking your prep:</h2>
		<p>You must take the second part of your prep 4 hours before your test, even if that means getting up very early. We know it is hard to get up this early, but it is important that you take the second part of your prep four hours before your colonoscopy for the prep to work.</p>

		<br/>

		<div class="alert alert-info">
			<p>For example, If your colonoscopy is scheduled for 10:00 AM in the morning, you need to take the second part of your prep at 6:00 AM.</p>
		</div>
