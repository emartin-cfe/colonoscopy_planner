		<h2 class="top">FAQs About Your Meds</h2>

		<p>Below, we provide instructions about some common medicines. However, if you still have any questions or concerns about them, please check with your doctor.</p>

		<h2>What if I take fish oil?</h2>
		<p>STOP taking fish oil two days before your colonoscopy. You can continue taking this after the test, unless otherwise instructed by your doctor.</p>

		<h2>What if I take diabetes medicine?</h2>
		<p>If you have diabetes, take your diabetes medicine until the night BEFORE your colonoscopy. Then, do not take your diabetes medicine on the morning of the test. You will resume these medicines after the test.</p>

		<h2>What if I take blood-pressure medicine?</h2>
		<p>If you take blood-pressure medicine, be sure to still use the medicine while preparing for the test. On the day of your test, you should take your blood-pressure medicine with water at least two hours before your test.</p>
