		<h2 class="top">Monday instructions: <span class="red">1 day before</span> your colonoscopy</h2>

		<h2>What you eat:</h2>
		<p>You <b>must not</b> eat any <span class="image_popup"><a class="image_popup" href="images/foods_not_ok.png" title="Solid food makes it hard for the GI doctor to see inside.">solid foods</a> the day before your colonoscopy.</p>

		<h2>What you drink:</h2>
		<p>You <b>must</b> drink only <span class="image_popup"><a class="image_popup" href="images/drinks_ok.png" title="Do NOT drink cloudy liquids: it will make it hard for the GI doctor to     see inside.">clear liquids</a></span> for breakfast, lunch, and dinner. Drink 12 full cups of clear liquids throughout the day in addition to what you drink with your bowel prep.</p>

		<h2>Taking your bowel prep:</h2>
		<p>By the day before your test you should already have your "bowel prep" medicine. If not, call your doctor. The instructions are located inside the prep box. <b>Take the first part of your prep at 6:00 p.m.</b></p>

		<div class="alert alert-info">
			<p>Dissolve your prep in the morning and chill it in the fridge: it will taste better.</p>
		</div>
