		<h2 class="top">Instructions: One day before your colonoscopy</h2>

		<h2>What you eat:</h2>
		<p>You must not eat any <span class="image_popup"><a class="image_popup" href="images/foods_not_ok.png" title="Solid food makes it hard for the GI doctor to see inside.">solid foods</a> the day before your colonoscopy.</p>

		<h2>What you drink:</h2>
		<p>You must drink only <span class="image_popup"><a class="image_popup" href="images/drinks_ok.png" title="Do NOT drink cloudy liquids: it will make it hard for the GI doctor to     see inside.">clear liquids</a></span> for breakfast, lunch, and dinner. Drink 12 full cups of clear liquids throughout the day in addition to what you drink with your bowel prep.</p>

		<h2>Taking your prep:</h2>
		<p>By the day before your test you should already have your "bowel prep" medicine. If not, call your doctor. The instructions are located inside the prep box. <b>Take the first part of your prep at 6:00 p.m. the evening before your test</b>.</p>

