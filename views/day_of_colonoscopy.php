		<h2 class="top">DAY of Your Colonoscopy</h2>

		<h2>What You Eat:</h2>
		<p>You must not eat any solid foods prior to your colonoscopy, even if your colonoscopy is scheduled for the afternoon.</p>

		<h2>What You Drink:</h2>
		<p>You must drink only clear liquids before your colonoscopy. Be sure to drink at least two tall glasses (at least 8-10 ounces each) of clear sports drink with electrolytes prior to your colonoscopy.</p>

		<h2>Taking Your Prep:</h2>
		<p>You must take the second part of your prep four hours before your test - even if that means getting up very early. We know it is hard to get up this early, but it is important that you take the second part of your prep four hours before your colonoscopy for the prep to work.</p>

		<br/>

		<div class="alert alert-info">
			<p class="large">An example</p>
			<p>If your colonoscopy is scheduled for 10:00 AM in the morning, you will need to take the second part of your prep at 6:00 AM in the morning.</p>
		</div>
