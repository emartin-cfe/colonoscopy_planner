                <h2>How do I know I've drank enough clear liquids?</h2>
                <p>The stool coming out should look like the stuff you are eating and drinking - clear, <b>without many particles.</b> You know you're done when the stool coming out is <b>yellow, light, liquid, and clear - </b> like urine.</p>
