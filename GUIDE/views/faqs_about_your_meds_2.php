		<h2 class="top">FAQs About Your Meds (Part 2)</h2>

		<p>If you are taking any of the medicines below, please talk to your doctor about whether you should or should not continue to take them. You may be asked to stop certain medications before your colonoscopy.</p>

		<h2>What if I take an anti-inflammatory, like Ibuprofen or Advil?</h2>
		<p>You may be taking anti-inflammatory medicine such as Ibuprofen, Advil, Motrin, Aleve, Naprosyn, or Naproxen. You should be able to continue to take this medicine as needed, but please ask your doctor.</p>

		<h2>What if I take aspirin, clopidogrel, or Plavix?</h2>
		<p>You should be able to continue to take this medicine as needed, but please ask your doctor.</p>

